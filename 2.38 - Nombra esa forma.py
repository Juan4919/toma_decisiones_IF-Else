lados = int(input("Ingresa el numero de lados del polígono: "))
if lados == 3:
    print("El polígono es un triángulo")
elif lados == 4:
    print("El polígono es un cuadrilatero")
elif lados == 5:
    print("El polígono es un pentágono")
elif lados == 6:
    print("El polígono es un hexagono")
elif lados == 7:
    print("El polígono es un heptágono")
elif lados == 8:
    print("El polígono es un octógono")
elif lados == 9:
    print("El polígono es un enágono")
elif lados == 10:
    print("El polígono es un decágono")
elif lados > 10:
    print("No estoy preparado para citar polígonos con ese número de lados")
     